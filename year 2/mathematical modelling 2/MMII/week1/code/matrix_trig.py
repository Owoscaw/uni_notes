import numpy as np

M = np.array([[1,0.5],[0.25,0.125]])

print("M**2=", M**2)
print("sin(M)=", np.sin(M*np.pi))
print("cos(M)=", np.cos(M*np.pi))
print("tan(M)=", np.tan(M*np.pi))
print("arcsin(M)=", np.arcsin(M))
print("exp(M)=", np.exp(M))
print("log(M)=", np.log(M))
print("np.sqrt(M)=", np.sqrt(M))
